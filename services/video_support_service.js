function getAreaLabel(area) {
  return area || '全身';
}

function buildYoutubeSearchUrl(query) {
  return `https://www.youtube.com/results?search_query=${encodeURIComponent(query)}`;
}

function getVideoGuide(area = '全身') {
  const a = getAreaLabel(area);

  if (a === '腰') {
    return {
      title: '腰まわりのやさしい動画案内',
      url: buildYoutubeSearchUrl('腰 ストレッチ やさしい 1分'),
      queryLabel: '腰 ストレッチ やさしい 1分',
    };
  }

  if (a === '膝') {
    return {
      title: '膝まわりのやさしい動画案内',
      url: buildYoutubeSearchUrl('膝 ストレッチ やさしい 股関節 1分'),
      queryLabel: '膝 ストレッチ やさしい 股関節 1分',
    };
  }

  if (a === '股関節') {
    return {
      title: '股関節のやさしい動画案内',
      url: buildYoutubeSearchUrl('股関節 ストレッチ やさしい 1分'),
      queryLabel: '股関節 ストレッチ やさしい 1分',
    };
  }

  if (a === '肩' || a === '首') {
    return {
      title: '首肩まわりのやさしい動画案内',
      url: buildYoutubeSearchUrl('首 肩 ストレッチ やさしい 1分'),
      queryLabel: '首 肩 ストレッチ やさしい 1分',
    };
  }

  if (a === 'ふくらはぎ' || a === '足首') {
    return {
      title: 'ふくらはぎ・足首のやさしい動画案内',
      url: buildYoutubeSearchUrl('ふくらはぎ 足首 ストレッチ やさしい 1分'),
      queryLabel: 'ふくらはぎ 足首 ストレッチ やさしい 1分',
    };
  }

  return {
    title: '全身のやさしい動画案内',
    url: buildYoutubeSearchUrl('全身 ストレッチ やさしい 3分'),
    queryLabel: '全身 ストレッチ やさしい 3分',
  };
}

function buildVideoSupportResponse(area = '全身') {
  const guide = getVideoGuide(area);
  const text = [
    `${guide.title}です。`,
    '無理なくできそうなものから選んでくださいね。',
    `動画検索はこちら: ${guide.url}`,
  ].join('\n');

  return {
    text,
    quickReplies: ['1分メニュー', '3分メニュー', 'やさしい版', '説明だけ聞く', '今日はここまで'],
  };
}

