function isVideoIntent(text) {
  const t = String(text || '');
  return [
    '動画で見たい',
    '動画',
    '見たい',
    '腰の動画',
    '膝の動画',
    '股関節の動画',
    '首肩の動画',
    'ふくらはぎの動画',
  ].includes(t);
}

